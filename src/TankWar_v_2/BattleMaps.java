    public void paintMap(Graphics2D g2d) {
        brickImage[0] = new ImageIcon("D:\\桌面\\Xing\\Photos\\Bricks.png");
        int mapX = 120;
        int mapY = 60;
        for (int x = 0; x < rows; x++) {
            for (int y = 0; y < cols; y++) {
                int blockType = map1[x][y];
                if (blockType == Brick) {
                    g2d.drawImage(brickImage[0].getImage(), mapX, mapY, 15, 15, null);
                }
                mapX += 15;
            }
            mapY += 15;
            mapX = 120;
        }
    }

    public boolean isCollidingWithWall(Rectangle rect) {
        //计算地图块起始坐标（地图绘制起始点）
        int mapStartX = 120;
        int mapStartY = 60;
        int tileSize = 15;

        // 获取子弹/坦克在地图上的位置   将坦克的矩形转换为单位地图块索引范围
        int startCol = (rect.x - mapStartX) / tileSize;
        int startRow = (rect.y - mapStartY) / tileSize;
        int endCol = (rect.x + rect.width - mapStartX - 1) / tileSize;
        int endRow = (rect.y + rect.height - mapStartY - 1) / tileSize;

        //防止索引越界
        startCol = Math.max(startCol, 0);
        startRow = Math.max(startRow, 0);
        endCol = Math.min(endCol, cols - 1);
        endRow = Math.min(endRow, rows - 1);

        for (int row = startRow; row <= endRow; row++) {
            for (int col = startCol; col <= endCol; col++) {
                if (row < map1.length && col < map1[row].length) {
                    if (map1[row][col] == Brick) {
                        Rectangle wallRect = new Rectangle(// 创建墙体的矩形区域
                                mapStartX + col * tileSize,
                                mapStartY + row * tileSize,
                                tileSize,
                                tileSize
                        );
                        if (rect.intersects(wallRect)) {// 检查对象是否与墙体相交
                            return true;
                        }
                    }
                }
            }
        }
        return false;
    }
