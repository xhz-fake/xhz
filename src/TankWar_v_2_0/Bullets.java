package TankWar_v_2_0;

public class Bullets {
    private final int size;
    private final int speed;

    public Bullets(int size, int speed) {
        this.size = size;
        this.speed = speed;
    }

    public int getSize() {
        return size;
    }

    public int getSpeed() {
        return speed;
    }

}
